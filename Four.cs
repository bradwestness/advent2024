    public string ExecutePartOne(string input)
    {
        const string word = "XMAS";

        var grid = ParseInput(input);

        var total = CountOccurrences(grid, word);

        return $"Total number of 'XMAS' occurances: {total}";
    }

    public string ExecutePartTwo(string input)
    {
        var grid = ParseInput(input);
        int total = 0;

        for (var i = 1; i < grid.GetLength(0) - 1; i++)
        {
            for (var j = 1; j < grid.GetLength(1) - 1; j++)
            {
                if (IsMasX(grid, i, j))
                {
                    total++;
                }
            }
        }

        return $"Total number of 'MAS' Xs: {total}";
    }

    private static bool IsMasX(char[,] grid, int x, int y)
    {
        if (!IsValidCoordinate(x, y, grid.GetLength(0), grid.GetLength(1))
            || !IsValidCoordinate(x - 1, y - 1, grid.GetLength(0), grid.GetLength(1))
            || !IsValidCoordinate(x - 1, y + 1, grid.GetLength(0), grid.GetLength(1))
            || !IsValidCoordinate(x + 1, y - 1, grid.GetLength(0), grid.GetLength(1))
            || !IsValidCoordinate(x + 1, y + 1, grid.GetLength(0), grid.GetLength(1)))
        {
            return false;
        }

        if (grid[x, y] != 'A')
        {
            return false;
        }

        if (IsMas(grid[x - 1, y - 1], grid[x, y], grid[x + 1, y + 1])
            && IsMas(grid[x - 1, y + 1], grid[x, y], grid[x + 1, y - 1]))
        {
            return true;
        }

        return false;
    }

    private static bool IsMas(char a, char b, char c)
    {
        var str = new string([a, b, c]);
        return str == "MAS" || str == "SAM";
    }

    private static int CountOccurrences(char[,] grid, string word)
    {
        int[] x = { -1, -1, -1, 0, 0, 1, 1, 1 };
        int[] y = { -1, 0, 1, -1, 1, -1, 0, 1 };
        int occurrences = 0;

        for (int i = 0; i < grid.GetLength(0); i++)
        {
            for (int j = 0; j < grid.GetLength(1); j++)
            {
                for (int k = 0; k < 8; k++)
                {
                    if (FindWord(0, word, grid, i, j, x[k], y[k]))
                    {
                        occurrences++;
                    }
                }
            }
        }

        return occurrences;
    }

    private static bool FindWord(int index, string word, char[,] grid, int x, int y, int dirX, int dirY)
    {
        if (index == word.Length)
        {
            return true;
        }

        if (IsValidCoordinate(x, y, grid.GetLength(0), grid.GetLength(1)) && word[index] == grid[x, y])
        {
            return FindWord(index + 1, word, grid, x + dirX, y + dirY, dirX, dirY);
        }

        return false;
    }

    private static bool IsValidCoordinate(int x, int y, int m, int n)
    {
        return x >= 0 && x < m && y >= 0 && y < n;
    }

    private static char[,] ParseInput(string input)
    {
        var lines = input.Split(Environment.NewLine, StringSplitOptions.TrimEntries | StringSplitOptions.RemoveEmptyEntries);
        char[,] result = new char[lines.Length, lines[0].Length];

        for (var x = 0; x < lines.Length; x++)
        {
            for (var y = 0; y < lines[x].Length; y++)
            {
                result[x, y] = lines[x][y];
            }
        }

        return result;
    }

